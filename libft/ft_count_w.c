int	ft_count_w(const char *str, char c)
{
	int		s;

	while (*str != '\0' && *str == c)
		++str;
	if (*str != '\0')
		s = 1;
	else
		s = 0;
	while (*str != '\0')
	{
		if (str[0] == c && str[1] && str[1] != c)
			s++;
		str++;
	}
	return (s);
}
