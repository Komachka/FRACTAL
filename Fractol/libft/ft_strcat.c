#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	int len1;
	int len2;

	len1 = 0;
	len2 = 0;
	while (s1[len1] != '\0')
		len1++;
	while (s2[len2] != '\0')
		len2++;
	while (len2--)
		s1[len1++] = *s2++;
	s1[len1++] = '\0';
	return (s1);
}
